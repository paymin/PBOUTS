    private void selectData() {
        String kolom[] = {"Id Transaksi","Tanggal","Kasir","Id Barang","Nama Barang","Jumlah Barang","Harga Barang","Diskon","Total"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = koneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String id_transaksi = rs.getString(1);
                String tanggal = rs.getString(2);
                String kasir = rs.getString(3);
 
               String id_barang= rs.getString(4);
               String nama_barang = rs.getString(5);
               String harga_barang = rs.getString(6);
               String diskon = rs.getString(8);
               String jumlah_barang = rs.getString(7);
               String total = rs.getString(9);
               String data[] = {id_transaksi,tanggal,kasir,id_barang,nama_barang,jumlah_barang,harga_barang,diskon,total};
               dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm); //To change body of generated methods, choose Tools | Templates.
    }
