    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(Tanggal.getDate());
        if("".equals(cbKasir.getSelectedItem()) || "".equals(cbBarang.getSelectedItem()) ||
                "".equals(tfBarang.getText()) || "".equals(tfHarga.getText())  || "".equals(Tanggal) 
                || "".equals(tfJumlah.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            int bilangan1 = Integer.parseInt(String.valueOf(tfHarga.getText()));
            int bilangan2 = Integer.parseInt(String.valueOf(tfJumlah.getText()));
            int total = bilangan1*bilangan2;
            int diskon = 0;
            
            if(total > 100000){
                diskon = 10;
            } else if (total > 250000){
                diskon = 15;
            }
            
            total = total - (total * diskon/100);
            
            String SQL = "INSERT INTO tb_transaksi (tanggal,kasir,id_barang,nama_barang,jumlah_barang,harga_barang,diskon,total)" 
                    + "VALUES('"+tanggal+"','"+cbKasir.getSelectedItem()+"','"+cbBarang.getSelectedItem()+"','" + tfBarang.getText()+"','"
                    + tfHarga.getText()+"','"+ tfJumlah.getText()+"','"+diskon+"','"+total+"')";
            int status = koneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btSaveActionPerformed

